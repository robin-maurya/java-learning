package day02.concepts;

public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;

        System.out.println("Initial value: " +a);

        a+= 5;
        System.out.println("after += :" + a);

        a -= 3;
        System.out.println("after -= :" + a);

        a*= 2;
        System.out.println("after -= :" + a);

        a /= 2;
        System.out.println("after /= :" + a);

        a %= 5;
        System.out.println("after %= :" + a);

    }
}
