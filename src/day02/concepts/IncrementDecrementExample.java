package day02.concepts;

public class IncrementDecrementExample {
    public static void main(String[] args) {

        int a = 10;

        a++;
        System.out.println("a++ : " + a);

        a--;
        System.out.println("a-- : " + a);

        ++a;
        System.out.println("++a : " + a);

        --a;
        System.out.println("--a : " + a);
    }
}
